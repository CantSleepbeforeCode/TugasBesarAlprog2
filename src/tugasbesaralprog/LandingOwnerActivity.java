    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            connection = koneksi.getKoneksi();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM produk");
            
            XSSFWorkbook wb = new XSSFWorkbook();
            
            Sheet sheet = wb.createSheet();
            
            Row row = sheet.createRow(0);
            
            String[] headerCell = {"Nama Produk", "Jumlah Produk", "Harga Beli", "Harga Jual", "Benefit"};
            for(int i = 0; i < headerCell.length; i++) {
                Cell cell = row.createCell(i);
                cell.setCellValue(headerCell[i]);
            }
            
            int noUrut = 1;
            
            while(resultSet.next()) {
                Row contentRow = sheet.createRow(noUrut++);
                
                String[] valueRow = {
                    resultSet.getString("nama_product"),
                    resultSet.getString("jumlah_product"),
                    resultSet.getString("harga_jual"),
                    resultSet.getString("harga_beli"),
                    resultSet.getString("benefit")
                };
                for(int i = 0; i < valueRow.length; i++) {
                    Cell contentCell = contentRow.createCell(i);
                    contentCell.setCellValue(valueRow[i]);
                }
            }
            
            String pattern = "MM-dd-yyyy";
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
            String date = simpleDateFormat.format(new Date());
            
            FileOutputStream fileOut = new FileOutputStream("D:\\Kuliah\\SEMESTER 3\\Alprog\\tubes\\xlsx\\" + username + "_" + date + ".xlsx");
            
            wb.write(fileOut);
            
            fileOut.close();
        } catch (IOException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Maaf, Terjadi masalah", "Warehouse.in", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
