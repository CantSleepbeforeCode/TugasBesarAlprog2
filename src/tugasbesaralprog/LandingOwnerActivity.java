    public void showDataProduk() {
        for(int i = tbl_data.getRowCount() - 1; i >= 0; i--) {
            tabelModelProduk.removeRow(i);
        }
        
        try {
            connection = koneksi.getKoneksi();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM produk");
            
            int noUrut = 1;
            
            while(resultSet.next()) {
                String[] row = {
                    Integer.toString(noUrut++), 
                    resultSet.getString("nama_product"),
                    resultSet.getString("jumlah_product"),
                    resultSet.getString("harga_jual"),
                    resultSet.getString("harga_beli"),
                    resultSet.getString("benefit")
                };
                
                tabelModelProduk.addRow(row);
            }
            
            tbl_data.setModel(tabelModelProduk);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Maaf, Tampil data tidak berhasil", "Warehouse.in", JOptionPane.PLAIN_MESSAGE);
        }
        
        lbl_benefit.setVisible(true);
        
        double benefit= 0;
        
        for (int j = 0; j  < tbl_data.getRowCount(); j++) {
            String temp = String.valueOf(tbl_data.getValueAt(j, 5));
            benefit += Double.parseDouble(temp);
        }
        
        if(lbl_benefit.getText().length() == 30) {
            lbl_benefit.setText(String.format("%s %s", lbl_benefit.getText(), indonesianCurrency(benefit)));
        }
    }
    
    public void showDataKaryawan() {
        for(int i = tbl_karyawan.getRowCount() - 1; i >= 0; i--) {
            tabelModelKaryawan.removeRow(i);
        }
        
        LoginActivity getData = new LoginActivity();
        String[][] listUser = getData.getUser();
        
        int noUrut = 1;
        
        for (String[] listUser1 : listUser) {
            if (listUser1[2].equals("employee")) {
                String[] row = {Integer.toString(noUrut++), listUser1[0]};
                tabelModelKaryawan.addRow(row);
            }
        }
        
        tbl_karyawan.setModel(tabelModelKaryawan);
        
        lbl_benefit.setVisible(false);
    }
    
    private String indonesianCurrency(double nominal) {
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');

        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        return kursIndonesia.format(nominal); 
    }
    
